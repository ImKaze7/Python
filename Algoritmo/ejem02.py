#Un program que sume, reste, multiplique, y divida

#Ingresante
numero01 = int(input("Ingrese el primer numero: "))
numero02 = int(input("Ingrese el segundo numero: "))

#Cálculo
suma = numero01 +numero02 
resta = numero01-numero02
multiplicación = numero01 * numero02
division = numero01 / numero02

#Resultado
print("La suma es: ",suma)
print("La resta es: ",resta)
print("La multiplicación es: ",multiplicación)
print("La división es: ", division)



